import Foundation

struct Dashboard: Codable {
    let frame: Frame
    let heading: String = "Dashboard"
    let description: String = ""
    let content = "CONTENT"
    
    init(user: User) {
        self.frame = Molucules.frame(user: user)
    }
}

extension Views {
    
    static func dashboard(user: User) -> Dashboard {
        return Dashboard(user: user)
    }
}

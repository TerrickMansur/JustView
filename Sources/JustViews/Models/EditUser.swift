import Foundation


struct EditUserView: Codable {

    let frame: Frame
    
    init(user: User) {
        self.frame = Molucules.frame(user: user, root: "../")
    }
}

extension Views {
    static func editUser(user: User) -> EditUserView {
        return EditUserView(user: user)
    }
}
